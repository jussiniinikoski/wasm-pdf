use super::encoders::winansi;
use lazy_static;
use std::collections::BTreeMap;

#[derive(Debug, PartialEq, Eq, Hash, Clone)]
pub struct Font {
    name: String,
    widths: BTreeMap<u8, u16>,
    font_ref: String,
}

impl Font {
    fn add_width(&mut self, name: &'static str, width: u16) {
        if let Some(code) = winansi::get_code(name) {
            self.widths.insert(code, width);
        }
    }

    /// Get the width of a specific, encoded character.
    pub fn get_character_width(&self, char: u8) -> Option<u16> {
        match self.widths.get(&char) {
            Some(&w) => Some(w),
            None => None,
        }
    }

    pub fn get_name(&self) -> String {
        self.name.clone()
    }
    pub fn get_ref(&self) -> String {
        self.font_ref.clone()
    }

    pub fn get_width(&self, size: f32, text: &str) -> f32 {
        size * self.get_width_raw(text) as f32 / 1000.0
    }

    pub fn get_width_raw(&self, text: &str) -> u32 {
        let mut result = 0;
        for char in text.chars() {
            result += u32::from(
                winansi::encode_char(char)
                    .and_then(|ch| self.get_character_width(ch))
                    .unwrap_or(100),
            );
        }
        result
    }
}

pub fn get_font(name: &str) -> &'static Font {
    match name {
        "helvetica" => helvetica(),
        "courier" => courier(),
        "times" => times_roman(),
        "helvetica-bold" => helvetica_bold(),
        "helvetica-oblique" => helvetica_oblique(),
        "helvetica-bold-oblique" => helvetica_bold_oblique(),
        "courier-bold" => courier_bold(),
        "courier-oblique" => courier_oblique(),
        "courier-bold-oblique" => courier_bold_oblique(),
        "times-bold" => times_bold(),
        "times-italic" => times_italic(),
        "times-bold-italic" => times_bold_italic(),
        _ => helvetica(), // default font
    }
}

